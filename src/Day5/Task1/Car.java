package Day5.Task1;

public class Car {
    String model;
    String color;
    int year;

    public String getModel(){
        return this.model;
    }

    public String getColor(){
        return this.color;
    }

    public int getYear(){
        return this.year;
    }

    public void setModel(final String model){
        this.model = model;
    }

    public void setColor(final String color){
        this.color = color;
    }

    public void setYear(final int year){
        this.year = year;
    }
}
